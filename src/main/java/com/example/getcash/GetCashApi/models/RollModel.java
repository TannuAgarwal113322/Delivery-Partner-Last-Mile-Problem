package com.example.getcash.GetCashApi.models;

public class RollModel {
	private String email;
	private String primaryRoll;
	private String temporaryRoll;

	public String getEmail() {
		return email;
	}

	public void setEmail(String email) {
		this.email = email;
	}

	public String getPrimaryRoll() {
		return primaryRoll;
	}

	public void setPrimaryRoll(String primaryRoll) {
		this.primaryRoll = primaryRoll;
	}

	public String getTemporaryRoll() {
		return temporaryRoll;
	}

	public void setTemporaryRoll(String temporaryRoll) {
		this.temporaryRoll = temporaryRoll;
	}

}
